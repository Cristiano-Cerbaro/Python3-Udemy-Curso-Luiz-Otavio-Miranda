"""
Operadores aritmeticos
+ , - , * , / , // divisao arredonda inteiro,
** um numero elevado a outro ,
% resto da divisao, ()
"""
print(10 * 10)
print(10 + 10)
print(10 - 5)
print(10 / 2)
print(11.6 / 2)
print(11.6 // 2)  # arredonda pra baixo
print('5' + '5')  # concatena strings
print(10 % 3)  # resto da divisao
print(20*'A')  # repete 20 vezes a letra A
